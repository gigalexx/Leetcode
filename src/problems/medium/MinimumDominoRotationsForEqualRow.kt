fun minDominoRotations(A: IntArray, B: IntArray): Int {

    val p1 = A[0]
    val p2 = B[0]


    return if (p1 == p2) count(A, B, p1)
    else kotlin.math.max(count(A, B, p1), count(A, B, p2))
}

fun count(A: IntArray, B: IntArray, target: Int): Int {
    var bot = 0
    var top = 0

    for (index in A.indices) {
        if (A[index] != target && B[index] != target) return -1
        else if (A[index] != target) top++
        else if (B[index] != target) bot++
    }
    return kotlin.math.min(bot, top)
}
